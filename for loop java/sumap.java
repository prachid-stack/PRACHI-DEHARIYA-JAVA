public class sumap { 
   public static void main(String[] args) { 
int a = 1, d = 3, n = 5; 
int sum = 0; 
int current = a; 
for(int i = 1; i <= n; i++) { 
sum += current; 
current += d; 
       } 
       System.out.println("Sum: " + sum); 
   } 
}


